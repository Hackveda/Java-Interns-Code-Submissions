public class For_loop {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        for(int i= 1;i<=5;i++){
            for(int j= 1;j<=10;j++){
                System.out.println("value of i:"+i+"value of j:"+j+".");
            }
            System.out.print("\n");
        }
    }
    
}
